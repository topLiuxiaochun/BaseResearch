package com.liuxc.export.beans;

public class MemberUnit {

	private String code;
	
	private String name;
	
	private String url;
	
	private String phone;
	
	private String SSEMemberUnit;//上证基金通会员单位
	
	private String htfFastLine;//公告代销添富快线
	
	private String SHETF;//上海国企ETF网下股票、现金发售

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getSSEMemberUnit() {
		return SSEMemberUnit;
	}

	public void setSSEMemberUnit(String sSEMemberUnit) {
		SSEMemberUnit = sSEMemberUnit;
	}

	public String getHtfFastLine() {
		return htfFastLine;
	}

	public void setHtfFastLine(String htfFastLine) {
		this.htfFastLine = htfFastLine;
	}

	public String getSHETF() {
		return SHETF;
	}

	public void setSHETF(String sHETF) {
		SHETF = sHETF;
	}

	
	
	
}
